/// Internationalization — English / Chinese.

#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum Language {
    English,
    Chinese,
}

impl Language {
    pub fn code(&self) -> &'static str {
        match self {
            Language::English => "en",
            Language::Chinese => "zh",
        }
    }

    #[allow(dead_code)]
    pub fn label(&self) -> &'static str {
        match self {
            Language::English => "English",
            Language::Chinese => "中文",
        }
    }

    pub fn from_code(code: &str) -> Self {
        match code {
            "zh" => Language::Chinese,
            _ => Language::English,
        }
    }
}

